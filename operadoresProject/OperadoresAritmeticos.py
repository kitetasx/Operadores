#Ejemnplos utilizando operadores aritmeticos
#suma
numero1 = int(input("Ingrese el numero1:"))
numero2 = int(input("Ingrese el numero2:"))

suma = numero1 + numero2
resta= numero1 - numero2
division = numero1 / numero2
multiplicacion = numero1 * numero2
modulo = numero1 % numero2
divisionentera= numero1 // numero2


print("La suma es: ",str(suma))
print("La resta es: ",str(resta))
print("La division es: ",str(division))
print("La multiplicacion es: ",str(multiplicacion))
print("El moduloes: ",str(modulo))
print("La divisionentera es: ",str(divisionentera))

print(f"La suma del {numero1}+{numero2}={suma}")
