#Ejemplos de operadores de asignacion

var1 = 5
var2 = 12
var3 = 8
var4 = 14
var5 = 6
var6 = 8

var1 += 3 #vari1 = vari+3  Asignacion con suma
print(f"Asignacion con suma: {var1}")

var2 -= 3 #vari2 = var2-3  Asignacion con resta
print(f"Asignacion con suma: {var2}")

var3 *= 3 #vari3 = var3*3  Asignacion con multiplicacion
print(f"Asignacion con suma: {var3}")


var4 /= 3 #vari4 = var4+3  Asignacion con sum
print(f"Asignacion con suma: {var4}")

var5 %= 3 #vari5 = var5%3  Asignacion con residuo
print(f"Asignacion con suma: {var5}")

var6 //= 3 #vari6 = var6//3  Asignacion con DIVIVION ENTERA
print(f"Asignacion con suma: {var6}")

print(var1)


